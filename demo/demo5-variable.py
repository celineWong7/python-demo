# 一、变量
# 在C、JAVA语言中，变量是强变量类型（也叫作静态语言），即声明变量是需要制定变量存储的数据类型；
# 在JavaScript，变量是弱变量类型（也叫作动态语言），即声明变量统一使用var声明，变量可以任意变换存储数据类型。
# 在Python中，是若变量类型。
# 
# 1. 动态语言
# 直接给出变量名，无需使用任何关键字去声明
#eg1
a = 1100 # a是整数
print(a)

a = 'abc' # a变成字符串
print(a)

